    private void createItem_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createItem_btnActionPerformed
        // TODO add your handling code here:           
        
        String itemID = itemID_input.getText();
        String name = name_input.getText();
        String price = price_input.getText();
        String stock = stock_input.getText();
        String supplied = supplier_combo.getSelectedItem().toString();
        
        if(itemID.equals("") || name.equals("") || price.equals("") || stock.equals("")){
            JOptionPane.showMessageDialog(null, "Please fill all the empty field!");
        }else if(checkID(itemID)){
            JOptionPane.showMessageDialog(null, "Supplier already registered!");
        }else if(checkName(name)){
            JOptionPane.showMessageDialog(null, "Supplier already registered!");
        }else if(itemID.length() > 4){
            JOptionPane.showMessageDialog(null, "ID can not be more than 4 character");
        }else{
        PreparedStatement ps;
        String query = "INSERT INTO `items` (`id_barang`, `namaBarang`, `hargaBarang`, `stockBarang`, `id_supplier`) VALUES (?,?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);        
            ps.setString(1, itemID);
            ps.setString(2, name);
            ps.setString(3, price);
            ps.setString(4, stock);
            ps.setString(5, supplied);
            
            if(ps.executeUpdate() > 0){
            JOptionPane.showMessageDialog(null, "New Items Added");
            Item item = new Item();
            item.setVisible(true);
            item.pack();
            item.setLocationRelativeTo(null);
            item.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Item.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }                                        

