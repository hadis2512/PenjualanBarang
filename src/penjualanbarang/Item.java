    private void fillTable(){
        try {
            PreparedStatement psID;
            ResultSet rs;   
            String idSupp = "SELECT * FROM `suppliers`";
            psID = MyConnection.getConnection().prepareStatement(idSupp);
            rs = psID.executeQuery();
            while(rs.next()){
                String name = rs.getString("id_supplier");
                supplier_combo.addItem(name);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Item.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
